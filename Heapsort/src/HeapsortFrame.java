    public class MyPanel extends JPanel {

        public void paintComponent(Graphics g) {
            super.paintComponent(g);
            
            if(t < spy.Snapshots.size()){               
                spy.Snapshots.get(t).paintWithColor(g);
                jLabel1.setText("Compares: " + spy.Snapshots.get(t).countCompares + "    Swaps: " + spy.Snapshots.get(t).countSwaps);
            }
            else
               spy.Snapshots.get(spy.Snapshots.size()-1).paintWithColor(g); 
        }
    }

