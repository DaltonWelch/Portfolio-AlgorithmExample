    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        t=0;
        jComboBox1.setEnabled(false);
        tick.start();
        sorter.sort(spy);
    }//GEN-LAST:event_startButtonActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        sorterType = jComboBox1.getSelectedItem().toString();
        if(sorterType.equals("Heap")){
            sorter = Sorters.heap;
            tick.stop();
            t=0;
            startButton.setEnabled(true);
        }
        if(sorterType.equals("Selection")){
            sorter = Sorters.selection;
            tick.stop();
            t=0;
            startButton.setEnabled(true);
        }
        if(sorterType.equals("Bubble")){
            sorter = Sorters.bubble;
            tick.stop();
            t=0;
            startButton.setEnabled(true);
        }
        if(sorterType.equals("Insert")){
            sorter = Sorters.insert;
            tick.stop();
            t=0;
            startButton.setEnabled(true);
        }

    }//GEN-LAST:event_jComboBox1ItemStateChanged

